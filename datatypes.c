#include <stdio.h>
int main(){
    int a = 15; //this is integer type
    printf("%d",a); //we use %d to  print  integer

    float b = 12.0; //this is float (decimal) type
    printf("%f",b);//we use %f to print float 

    char c= "Priyanshu"; //this is character type in double quoation not  single quotation
    printf("%c",c);// we use %c  to print  character
    
    double d = 15.2; // this  is double in this  we can use both float and integer
    printf("%lf",d); //we use %lf to print double 
}